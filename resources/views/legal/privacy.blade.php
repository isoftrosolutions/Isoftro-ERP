  <h1>Privacy Policy</h1>
  <p class="updated">Last updated: April 12, 2026</p>

  <p>iSoftro Pvt. Ltd. ("iSoftro", "we", "our", or "us") operates the iSoftro Academic ERP platform at <a href="https://isoftroerp.com">isoftroerp.com</a>. This Privacy Policy explains how we collect, use, disclose, and safeguard your information when you use our platform.</p>

  <h2>1. Information We Collect</h2>
  <p>We collect information that you provide directly to us, including:</p>
  <ul>
    <li>Institute registration details (name, address, contact information)</li>
    <li>User account information (name, email address, role)</li>
    <li>Student and staff records entered by institution administrators</li>
    <li>Fee and financial transaction data</li>
    <li>Attendance and academic records</li>
    <li>Communication preferences and settings</li>
  </ul>
  <p>We also automatically collect certain technical data: IP addresses, browser type, device identifiers, and usage logs for security and performance purposes.</p>

  <h2>2. How We Use Your Information</h2>
  <p>We use the information we collect to:</p>
  <ul>
    <li>Provide, operate, and maintain the iSoftro platform</li>
    <li>Process transactions and send related notices</li>
    <li>Send administrative communications (updates, security alerts)</li>
    <li>Respond to support requests</li>
    <li>Monitor and improve platform performance and security</li>
    <li>Comply with legal obligations under Nepal law</li>
  </ul>
  <p>We do <strong>not</strong> sell, trade, or rent your personal information or your students' data to third parties.</p>

  <h2>3. Data Storage and Security</h2>
  <p>All data is stored on secure cloud infrastructure. We implement:</p>
  <ul>
    <li>AES-256 encryption at rest</li>
    <li>TLS 1.3 encryption in transit</li>
    <li>Role-based access controls (RBAC)</li>
    <li>Automated daily backups with 30-day retention</li>
    <li>Regular security audits and vulnerability assessments</li>
  </ul>

  <h2>4. Data Retention</h2>
  <p>We retain your data for as long as your institution maintains an active subscription. Upon account termination, data is retained for 90 days to allow export, after which it is permanently deleted.</p>

  <h2>5. Sharing of Information</h2>
  <p>We may share your information only in these limited circumstances:</p>
  <ul>
    <li>With service providers who assist us in delivering the platform (e.g., hosting, email)</li>
    <li>To comply with applicable law, court orders, or governmental requests</li>
    <li>To protect the rights, property, or safety of iSoftro, our users, or the public</li>
  </ul>

  <h2>6. Children's Privacy</h2>
  <p>iSoftro is designed for use by educational institutions. Student records, including those of minors, are entered and controlled solely by the institution. iSoftro does not directly collect personal information from children.</p>

  <h2>7. Your Rights</h2>
  <p>Institution administrators may request access to, correction of, or deletion of personal data by contacting us. We will respond within 30 days.</p>

  <h2>8. Cookies</h2>
